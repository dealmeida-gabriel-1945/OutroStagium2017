    private void miCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_miCadastrarActionPerformed
        boolean campos = this.verificaCampos();

        if (campos == true) {
            CursoBEAN curso = new CursoBEAN();

            curso.setNome(tfNome.getText());
            curso.setArea(tfArea.getText());
            curso.setDescricao(taDescricao.getText());

            boolean r = curControle.cadastrar(curso);

            if (r == true) {
                JOptionPane.showMessageDialog(null, "Curso cadastrado com sucesso!!!");
            } else if (r == false) {
                JOptionPane.showMessageDialog(null, "Erro ao Cadastrar: Curso já cadastrado");
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao Cadastrar!!!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao Cadastrar: Campos vazios!!!");
        }
    }//GEN-LAST:event_miCadastrarActionPerformed

